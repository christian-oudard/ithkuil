def make_abbreviation_table():
    abbreviation_table = {}
    for category_name, category_abbreviations in abbreviations.items():
        for abbr, name in category_abbreviations.items():
            full_name = '{} {}'.format(name, category_name).title()
            assert abbr not in abbreviation_table # Check for duplication.
            abbreviation_table[abbr] = full_name
    return abbreviation_table
abbreviation_table = make_abbreviation_table()

def verify_abbreviations():
    from .grammar import categories
    all_abbreviations = [
        a for c in categories for a in c
    ]
    assert sorted(all_abbreviations) == sorted(abbreviation_table.keys())
verify_abbreviations()

